package com.labwork2;

/**
 * CoordinateBridge interface
 */
public interface CoordinateBridge {
    /**
     * Set ro
     * @param ro new ro value
     */
    void setRo(double ro);

    /**
     * Set phi
     * @param phi new phi value
     */
    void setPhi(double phi);

    /**
     * Get ro
     * @return ro
     */
    double getRo();
    /**
     * Get phi
     * @return phi
     */
    double getPhi();
}
