package com.labwork2;

/**
 * Point interface
 */
public interface Point {
    /**
     * Set x coordinate
     * @param x x coordinate
     */
    void setX(int x);

    /**
     * Set y coordinate
     * @param y y coordinate
     */
    void setY(int y);

    /**
     * Get x coordinate
     * @return x coordinate
     */
    int getX();

    /**
     * Get y coordinate
     * @return y coordinate
     */
    int getY();
}
